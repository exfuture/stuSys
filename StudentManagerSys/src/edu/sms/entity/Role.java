package edu.sms.entity;

public class Role {
	
	private String	ROLE_ID;
	private String	ROLE_NAME;
	
	
	public String getROLE_ID() {
		return ROLE_ID;
	}
	public void setROLE_ID(String role_id) {
		ROLE_ID = role_id;
	}
	public String getROLE_NAME() {
		return ROLE_NAME;
	}
	public void setROLE_NAME(String role_name) {
		ROLE_NAME = role_name;
	}
	
	

}
